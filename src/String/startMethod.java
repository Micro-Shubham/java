package String;

public class startMethod {
    public static void main(String[] args) {
        String name = "billionair";
        //startwith method in string helps to find that those words are present or not in boolean .
        System.out.println(name.startsWith("billion"));
        //for ending words use endwith
        System.out.println(name.endsWith("r"));
    }
}
